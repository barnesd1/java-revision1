package calc;

public class Calculator {
	public Calculator() {
	}
	public Double Add(Double i, Double j) {
		return i + j;
	}
	public Double Subtract(Double i, Double j) {
		return i - j;
	}
	public Double Multiply(Double i, Double j) {
		return i * j;
	}
	public Double Divide(Double i, Double j) {
		return i / j;
	}
}
